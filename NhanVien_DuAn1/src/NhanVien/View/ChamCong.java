    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed

        if (tblmaNV.getText().isBlank()) {
            JOptionPane.showMessageDialog(this, "Bạn phải nhập mã NV muốn tìm trước");
            return;
        }
         
        int Thang = cbxThang.getSelectedIndex() + 1;
        int Nam = cbxNam.getSelectedIndex() + 2019;
      List<VM_BangCC> CV;
    try {
        List<VM_BangNV> NV = serviceNV.HienThitheoMa(tblmaNV.getText());
        if (NV.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Không tìm thấy nhân viên");
            return;
        }
        CV = service.HienThiTrongThang(String.valueOf(Thang) ,String.valueOf(Nam) ,tblmaNV.getText());
         DefaultTableModel model = (DefaultTableModel) tblChamCong.getModel();
        model.setRowCount(0);
        for (VM_BangCC nv : CV) {
            Object[] row = {nv.getIdCC(), nv.getIDNV(), nv.getTenNV(), nv.getNgayLam(), nv.getGioIN(), nv.getGioOUT(), nv.TrangThaiCC(),nv.TrangThaiTL()};
            model.addRow(row);
        }
    } catch (SQLException ex) {
        Logger.getLogger(ChamCong.class.getName()).log(Level.SEVERE, null, ex);
    }
       

    }//GEN-LAST:event_jButton1ActionPerformed

