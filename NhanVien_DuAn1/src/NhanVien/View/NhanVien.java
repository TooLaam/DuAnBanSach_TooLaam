    private void rdoTimActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_rdoTimActionPerformed
        if (TxtMaNV.getText().isBlank()) {
            JOptionPane.showMessageDialog(this, "Bạn phải nhập mã nhân viên trước");
            return;
        }
        try {
            // TODO add your handling code here:
            List<VM_BangNV> CV = serviceNV.HienThitheoMa(TxtMaNV.getText());
            if (CV.isEmpty()) {
                JOptionPane.showMessageDialog(this, "Không tìm thấy");
            } else {
                DefaultTableModel model = (DefaultTableModel) tblNhanVien.getModel();
                model.setRowCount(0);
                for (VM_BangNV nv : CV) {
                    Object[] row = {nv.getIDNV(), nv.getMa(), nv.getTen(), nv.getGioiTinh(), nv.getNgaySinh(), nv.getEmail(), nv.getSDT(), nv.getDiaChi(), nv.TrangThaiLam(), nv.TrangThaiship(), nv.getTenCV()};
                    model.addRow(row);
                }
            }
        } catch (SQLException ex) {
            Logger.getLogger(NhanVien.class.getName()).log(Level.SEVERE, null, ex);
        }

    }//GEN-LAST:event_rdoTimActionPerformed

