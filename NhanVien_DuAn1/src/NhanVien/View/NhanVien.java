    private void loadTableNV() throws SQLException {
        List<VM_BangNV> CV = serviceNV.HienThi();
        DefaultTableModel model = (DefaultTableModel) tblNhanVien.getModel();
        model.setRowCount(0);
        for (VM_BangNV nv : CV) {
            Object[] row = {nv.getIDNV(), nv.getMa(), nv.getTen(), nv.getGioiTinh(), nv.getNgaySinh(), nv.getEmail(), nv.getSDT(), nv.getDiaChi(), nv.TrangThaiLam(), nv.TrangThaiship(), nv.getTenCV()};
            model.addRow(row);
        }
    }

    private void loadTableCC() throws SQLException {
        List<VM_BangCC> CV = serviceCC.HienThi(String.valueOf(LocalDate.now()));
        DefaultTableModel model = (DefaultTableModel) tblChamCong.getModel();
        model.setRowCount(0);
        for (VM_BangCC nv : CV) {
            Object[] row = {nv.getIdCC(), nv.getIDNV(), nv.getTenNV(), nv.getNgayLam(), nv.getGioIN(), nv.getGioOUT(), nv.TrangThaiCC(), nv.TrangThaiTL()};
            model.addRow(row);
        }

    }

