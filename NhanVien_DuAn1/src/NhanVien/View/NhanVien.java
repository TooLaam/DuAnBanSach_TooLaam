            TxtMaNV.setText(ma);
            txtTen.setText(ten);
            txtDiaChi.setText(diachi);
            datePicker1.setText(ngaySinh);
            txtSDT.setText(SDT);
            txtEmail.setText(email);

            if (gioitinh.equals("Nam")) {
                rdoNam.setSelected(true);
            }
            if (gioitinh.equals("Nữ")) {
                rdoNu.setSelected(true);
            }
            if (trangtraiLamViec.equals("Đang làm")) {
                rdoDiLam.setSelected(true);
            }
            if (trangtraiLamViec.equals("Tạm thời nghỉ")) {
                rdoTamNghi.setSelected(true);
            }
            if (trangtraiLamViec.equals("Nghỉ việc")) {
                rdoNghiViec.setSelected(true);
            }
            if (trangtraiGiaoHang.equals("Có thể giao hàng")) {
                rdoCoGiao.setSelected(true);
            }
            if (trangtraiGiaoHang.equals("Không thể giao hàng")) {
                rdoKoGiao.setSelected(true);
            }
            if (chucVu.equals("Quản lý")) {
                rdoQuanLy.setSelected(true);
            }
            if (chucVu.equals("Nhân viên")) {
                rdoNhanVien.setSelected(true);
            }
