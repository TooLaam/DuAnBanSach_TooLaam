    private void jButton6ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton6ActionPerformed
        // TODO add your handling code here:

        int con = JOptionPane.showConfirmDialog(this, "Bạn muốn IN ca cho nhân viên có mã " + TxtMaNV.getText(), "Thông báo", JOptionPane.YES_NO_OPTION);
        if (con == JOptionPane.YES_OPTION) {
            if (txtIDNV.getText().isBlank()) {
                JOptionPane.showMessageDialog(this, "Bạn phải tìm ID nhân viên trước");
                return;
            } else {
                try {
                    List<VM_BangCC> CCC = serviceCC.CheckTinhTrangDiLam(txtIDNV.getText());
                    if (CCC.isEmpty()) {
                        JOptionPane.showMessageDialog(this, "Nhân viên có thể đang tạm thời nghỉ hoặc là đã nghỉ việc");
                        return;
                    } else {
                        try {

                            List<VM_BangCC> CV = serviceCC.CheckIN(String.valueOf(LocalDate.now()), txtIDNV.getText());
                            if (CV.isEmpty()) {
                                VM_BangCC cc = new VM_BangCC();
                                cc.setIDNV(txtIDNV.getText());
                                cc.setNgayLam(String.valueOf(LocalDate.now()));

                                cc.setTrangThai(1);
                                serviceCC.insert(cc);
                                loadTableCC();
                                JOptionPane.showMessageDialog(this, "IN thành công");

                            } else {
                                JOptionPane.showMessageDialog(this, "Nhân viên đang IN");
                                return;
                            }
                        } catch (SQLException ex) {
                            Logger.getLogger(NhanVien.class.getName()).log(Level.SEVERE, null, ex);
                        }

                    }
                } catch (SQLException ex) {
                    Logger.getLogger(NhanVien.class.getName()).log(Level.SEVERE, null, ex);
                }
            }
        }
    }//GEN-LAST:event_jButton6ActionPerformed

    private void jButton4ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton4ActionPerformed
        // TODO add your handling code here:

        int con = JOptionPane.showConfirmDialog(this, "Bạn muốn OUT ca cho nhân viên", "Thông báo", JOptionPane.YES_NO_OPTION);
        if (con == JOptionPane.YES_OPTION) {
            if (txtIDNV.getText().isBlank()) {
                JOptionPane.showMessageDialog(this, "Bạn phải tìm ID nhân viên trước");
                return;
            } else {
                try {

                    List<VM_BangCC> CV = serviceCC.CheckOUT(String.valueOf(LocalDate.now()), txtIDNV.getText());
                    List<VM_BangCC> CC = serviceCC.CheckIN(String.valueOf(LocalDate.now()), txtIDNV.getText());
                    if (CC.isEmpty()) {
                        JOptionPane.showMessageDialog(this, "Nhân viên chưa IN hoặc đã OUT");
                        return;
                    } else {
                        serviceCC.update(txtIDNV.getText(), String.valueOf(LocalDate.now()));
                        loadTableCC();
                        JOptionPane.showMessageDialog(this, "OUT thành công");
                    }
                } catch (SQLException ex) {
                    Logger.getLogger(NhanVien.class.getName()).log(Level.SEVERE, null, ex);
                }
            }
        }
    }//GEN-LAST:event_jButton4ActionPerformed

