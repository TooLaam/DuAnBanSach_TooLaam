public static boolean patternMatches(String emailAddress, String regexPattern) {
        return Pattern.compile(regexPattern)
                .matcher(emailAddress)
                .matches();
    }
    String regSDT = "^[0-9]{4}+$";
    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        String ma = txtLydo.getText();
        String ten = txtMuc.getText();

        int chon = JOptionPane.showConfirmDialog(this, "Bạn muốn thêm ???", "Thông báo", JOptionPane.YES_NO_OPTION);
        if (chon == JOptionPane.YES_OPTION) {
            if (txtLydo.getText().isBlank() || txtMuc.getText().isBlank()) {
                JOptionPane.showMessageDialog(this, "Bạn phải nhận đủ dữ liệu");
                return;
            }
            if (patternMatches(txtMuc.getText(), regSDT) == true) {
                JOptionPane.showMessageDialog(this, " Giá tiền phải là số và lớn hơn hoặc bằng 1000");
                return;
            } else {

                service.insertPhat(new VM_Phat(ma, "", BigDecimal.valueOf(Double.valueOf(ten))));
                JOptionPane.showMessageDialog(this, "Thêm thành công");
                try {
                    loadTable();
                } catch (SQLException ex) {
                    Logger.getLogger(PhieuThuong.class.getName()).log(Level.SEVERE, null, ex);
                }

                txtLydo.setText("");
                txtMuc.setText("");
                txtID.setText("");

            }
        }
    }//GEN-LAST:event_jButton1ActionPerformed

    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // TODO add your handling code here:
        int con = JOptionPane.showConfirmDialog(this, "Bạn muốn sửa dữ liệu", "Thông báo", JOptionPane.YES_NO_OPTION);
        if (con == JOptionPane.YES_OPTION) {
            if (tbl.getSelectedRow() == -1) {
                JOptionPane.showMessageDialog(this, "Bạn phải chọn dữ liệu muốn sửa");
                return;
            }
            if (patternMatches(txtMuc.getText(), regSDT) == false) {
                JOptionPane.showMessageDialog(this, " Giá tiền phải là số và lớn hơn hoặc bằng 1000");
                return;
            } else {
                String ID = txtID.getText();
                String LyDo = txtLydo.getText();
                String Muc = txtMuc.getText();

                try {
                    service.suaPhat(ID, LyDo, BigDecimal.valueOf(Double.valueOf(Muc)));
                } catch (SQLException ex) {
                    Logger.getLogger(PhieuThuong.class.getName()).log(Level.SEVERE, null, ex);
                }
                try {
                    loadTable();
                } catch (SQLException ex) {
                    Logger.getLogger(PhieuThuong.class.getName()).log(Level.SEVERE, null, ex);
                }
                JOptionPane.showMessageDialog(this, "Sửa thành công");
                txtID.setText("");
                txtLydo.setText("");
                txtMuc.setText("");

            }
        }
    }//GEN-LAST:event_jButton2ActionPerformed

    private void tblMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblMouseClicked
        // TODO add your handling code here:
        int index = tbl.getSelectedRow();
        if (index >= 0) {
            String ten = tbl.getValueAt(index, 2).toString();
            String ma = tbl.getValueAt(index, 1).toString();
            String id = tbl.getValueAt(index, 0).toString();
            txtLydo.setText(ma);
            txtMuc.setText(ten);
            txtID.setText(id);
        }
    }//GEN-LAST:event_tblMouseClicked

