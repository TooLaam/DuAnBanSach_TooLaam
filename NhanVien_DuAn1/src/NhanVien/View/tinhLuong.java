    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        // TODO add your handling code here:
        int con = JOptionPane.showConfirmDialog(this, "Bạn muốn tính lương", "Thông báo", JOptionPane.YES_NO_OPTION);
        if (con == JOptionPane.YES_OPTION) {

            if (txtIDNV.getText().isBlank() || txtTongTienCong.getText().isBlank() || txtThang.getText().isBlank()) {
                JOptionPane.showMessageDialog(this, "Bạn phải nhập đầy đủ thông tin");
                return;
            } else {
                try {
                    if (txtTongTienPhat.getText().isBlank() || txtTongTienThuong.getText().isBlank()) {
                        List<VM_TinhLuong> CV = service.check(txtIDNV.getText(), Integer.valueOf(txtThang.getText()));
                        if (!CV.isEmpty()) {
                            JOptionPane.showMessageDialog(this, "Nhân viên này đã được tính lương trong tháng");
                            return;
                        }

                        String idnv = txtIDNV.getText();
                        BigDecimal tongTienPhat = BigDecimal.valueOf(Double.valueOf(0));
                        BigDecimal tongTienThuong = BigDecimal.valueOf(Double.valueOf(0));
                        BigDecimal tongTienCong = BigDecimal.valueOf(Double.valueOf(txtTongTienCong.getText()));
                        int Thang = Integer.valueOf(txtThang.getText());
                        service.insertThuong(new VM_TinhLuong("", idnv, "", Thang, 0, tongTienPhat, tongTienThuong, tongTienCong, tongTienCong, tongTienCong));
                        try {
                            loadTableLuong();
                            JOptionPane.showMessageDialog(this, "Tính lương thành công");
                            service.suaTrangThaiCC(idnv, Thang);
                            service.suaTrangThaiTP(idnv, Thang);
                        } catch (SQLException ex) {
                            Logger.getLogger(tinhLuong.class.getName()).log(Level.SEVERE, null, ex);
                        }
                    } else {
                        List<VM_TinhLuong> CV = service.check(txtIDNV.getText(), Integer.valueOf(txtThang.getText()));
                        if (!CV.isEmpty()) {
                            JOptionPane.showMessageDialog(this, "Nhân viên này đã được tính lương trong tháng");
                            return;
                        }

                        String idnv = txtIDNV.getText();
                        BigDecimal tongTienPhat = BigDecimal.valueOf(Double.valueOf(txtTongTienPhat.getText()));
                        BigDecimal tongTienThuong = BigDecimal.valueOf(Double.valueOf(txtTongTienThuong.getText()));
                        BigDecimal tongTienCong = BigDecimal.valueOf(Double.valueOf(txtTongTienCong.getText()));
                        int Thang = Integer.valueOf(txtThang.getText());
                        service.insertThuong(new VM_TinhLuong("", idnv, "", Thang, 0, tongTienPhat, tongTienThuong, tongTienCong, tongTienCong, tongTienCong));
                        try {
                            loadTableLuong();
                            JOptionPane.showMessageDialog(this, "Tính lương thành công");
                            service.suaTrangThaiCC(idnv, Thang);
                            service.suaTrangThaiTP(idnv, Thang);
                        } catch (SQLException ex) {
                            Logger.getLogger(tinhLuong.class.getName()).log(Level.SEVERE, null, ex);
                        }

                    }
                    loadTableNV();
                    txtGioLam.setText("");
                    txtIDNV.setText("");
                    txtMaNV.setText("");
                    txtMucLuong.setText("");
                    txtSecond.setText("");
                    txtThang.setText("");
                    txtTongTienCong.setText("");
                    txtTongTienPhat.setText("");
                    txtTongTienThuong.setText("");

                } catch (SQLException ex) {
                    Logger.getLogger(tinhLuong.class.getName()).log(Level.SEVERE, null, ex);
                }
            }

        }
    }//GEN-LAST:event_jButton3ActionPerformed

