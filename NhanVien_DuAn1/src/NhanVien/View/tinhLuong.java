    private void loadTableNV() throws SQLException {
        List<VM_BangNV> CV = service.HienThi(cbxThang.getSelectedIndex() + 1);
        DefaultTableModel model = (DefaultTableModel) tblNhanVien.getModel();
        model.setRowCount(0);
        for (VM_BangNV nv : CV) {
            Object[] row = {nv.getIDNV(), nv.getMa(), nv.getTen(), nv.getGioiTinh(), nv.getNgaySinh(), nv.getEmail(), nv.getSDT(), nv.getDiaChi(), nv.TrangThaiLam(), nv.TrangThaiship(), nv.getTenCV()};
            model.addRow(row);
        }
    }

    private void loadTableTP() throws SQLException {
        int Thang = cbxThang.getSelectedIndex() + 1;
        int Nam = cbxNam.getSelectedIndex() + 2019;
        String maNV = tblmaNV.getText();
        List<VM_TinhLuong> CV = service.hienThiThuong(Thang, Nam, maNV);
        DefaultTableModel model = (DefaultTableModel) tblTongTienPhat.getModel();
        model.setRowCount(0);
        for (VM_TinhLuong nv : CV) {
            Object[] row = {nv.getIdNV(), nv.getMaNV(), nv.getTongTienPhat(), nv.getTongTienThuong()};
            model.addRow(row);
        }
    }

    private void loadTableCong() throws SQLException {
        int Thang = cbxThang.getSelectedIndex() + 1;
        int Nam = cbxNam.getSelectedIndex() + 2019;
        String maNV = tblmaNV.getText();
        List<VM_TinhLuong> CV = service.hienThiGioCong(Thang, Nam, maNV);
        DefaultTableModel model = (DefaultTableModel) tblCong.getModel();
        model.setRowCount(0);
        for (VM_TinhLuong nv : CV) {
            Object[] row = {nv.getIdNV(), nv.getMaNV(), nv.getMucLuong(), nv.getSecond()};
            model.addRow(row);
        }
    }

    private void loadTableLuong() throws SQLException {

        List<VM_TinhLuong> CV = service.hienThiTongLuong();
        DefaultTableModel model = (DefaultTableModel) tblBangLuong.getModel();
        model.setRowCount(0);
        for (VM_TinhLuong nv : CV) {
            Object[] row = {nv.getIdLuong(), nv.getIdNV(), nv.getMaNV(), nv.getThang(), nv.getTongTienPhat(), nv.getTongTienThuong(), nv.getTongTienCong(), nv.getTongLuong(), nv.TrangThaiTL()};
            model.addRow(row);
        }
    }

