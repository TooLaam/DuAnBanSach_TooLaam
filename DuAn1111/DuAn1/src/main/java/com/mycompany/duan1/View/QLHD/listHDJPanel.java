    private void hienthi(ArrayList<HoaDon> listHoaDon) {
        DefaultTableModel dtm = (DefaultTableModel) tblListHD.getModel();
        dtm.setRowCount(0);
        for (HoaDon listhd : listHoaDon) {
            dtm.addRow(new Object[]{listhd.getId(),listhd.getMa(),listhd.getTongTien(),listhd.getTthd().getTen()});
        }
    }
