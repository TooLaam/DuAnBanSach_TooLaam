    private void loadDataToTable() {
        dtm.setRowCount(0);
        for (CTSach ctsach : listCTSach) {
            dtm.addRow(new Object[]{ctsach.getIdCTSach(), ctsach.getSach().getTen(), ctsach.getNhaxuatban().getTen(), ctsach.getNamxuatban().getNam(),
                ctsach.getChatlieu().getTen(), ctsach.getNgonngu().getTen(), ctsach.getSoLuong(), ctsach.getGiaBan()});
        }
    }

