    private void tblDanhSachBookMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblDanhSachBookMouseClicked
        int index = tblDanhSachBook.getSelectedRow();
        Long idCTSach = (Long) tblDanhSachBook.getValueAt(index, 0);
        String tenSach = (String) tblDanhSachBook.getValueAt(index, 1);
        BigDecimal donGia = (BigDecimal) tblDanhSachBook.getValueAt(index, 7);
        CartItem cartitem = new CartItem(idCTSach, tenSach, 1, donGia);
        boolean exist = false;
        for (CartItem item : createHDJPanel.danhsachchon) {
            if (idCTSach.equals(item.getId())) {
                item.setSoLuong(item.getSoLuong() + 1);
                createHDJPanel.loadToTableSPMua(createHDJPanel.danhsachchon);
                exist = true;
                break;
            }
        }
        if (!exist) {
            createHDJPanel.danhsachchon.add(cartitem);
            createHDJPanel.loadToTableSPMua(createHDJPanel.danhsachchon);

        }
        createHDJPanel.logicTinhTongTienSauKhiTangOrGiamOrXoaSPTrongBang();

        //Set Số luong sách trong db sau khi them sách vào danh sách chọn
        CTSach cts = ctsachservice.findById(idCTSach);
        int soluongsachhientaitrongdb = cts.getSoLuong();
        int soLuongSachConLaiTrongDBSauKhiAnThem = soluongsachhientaitrongdb - 1;
        ctsachservice.updateSoLuongCTSach(idCTSach, soLuongSachConLaiTrongDBSauKhiAnThem);
        this.dispose();
    }//GEN-LAST:event_tblDanhSachBookMouseClicked

