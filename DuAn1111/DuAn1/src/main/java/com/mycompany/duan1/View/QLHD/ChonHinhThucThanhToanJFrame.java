    private void loadDataToTableTTTK() {
        List<ThongTinTK> listTk = new ArrayList();
        listTk.add(new ThongTinTK("Tran Tuan Linh", "Vietcombank", "001100"));
        listTk.add(new ThongTinTK("Tran Tuan Linh", "Viettinbank", "001100"));
        DefaultTableModel dtm = (DefaultTableModel) tblThongTinTK.getModel();
        dtm.setRowCount(0);
        for (ThongTinTK thongTinTK : listTk) {
            dtm.addRow(new Object[]{thongTinTK.getChuThe(), thongTinTK.getNganHang(), thongTinTK.getNganHang()});
        }
    }

