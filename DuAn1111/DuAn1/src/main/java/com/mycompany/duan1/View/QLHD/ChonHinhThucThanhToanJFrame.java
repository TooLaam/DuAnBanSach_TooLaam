    private void btnTaoQRCodeActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnTaoQRCodeActionPerformed
        DefaultTableModel dtm = (DefaultTableModel) tblThongTinTK.getModel();
        int indexRow = tblThongTinTK.getSelectedRow();
        if (indexRow == -1) {
            JOptionPane.showMessageDialog(null, "Vui lòng chọn Tài khoản");
            return;
        }
        String chuThe = (String) tblThongTinTK.getValueAt(indexRow, 0);
        String STK = (String) tblThongTinTK.getValueAt(indexRow, 1);
        String nganHang = (String) tblThongTinTK.getValueAt(indexRow, 2);
        double giaTriHD = Double.parseDouble(txtTongTienHĐ.getText());
        new QRCode(chuThe, STK, nganHang, giaTriHD).setVisible(true);


    }//GEN-LAST:event_btnTaoQRCodeActionPerformed

